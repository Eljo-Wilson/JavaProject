package pkg;

public class DataTypes {
	
	public static void main(String[] args) {
		//byte
		byte var1 = 121;
		System.out.println("Value of byte = "+var1);
		
		short var2 = 2345;
		System.out.println("value of short = "+var2);
		
		int var3 = 345656;
		System.out.println("value of int = "+var3);
	
		long var4 = 9868587885l; // when we giving long value we append l with the value
		System.out.println("value of long = "+var4);
	
		float var5 = 5.76f; // Here we append f to value
		System.out.println("value of float = "+var5);
	
		double var6 = 3.4567;
		System.out.println("value of double = "+var6);
	
		boolean var7 = 10 > 20;
		System.out.println("value of boolean = "+var7);
		
		char var8 = 'c';
		System.out.println("value of char = "+var8);
	
		String var9 = "string";  // we declare string with String(use S)
		System.out.println("value of string = "+var9);
				
	}

}
